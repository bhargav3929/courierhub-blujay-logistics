/**
 * scripts/test-merchant-webhook.mjs
 *
 * End-to-end smoke test for the merchant-webhook flow:
 *   1. Mints a fresh API key for a user (via firebase-admin, bypassing UI).
 *   2. POSTs a sample order to /api/integrations/orders/webhook with the key.
 *   3. Verifies the shipment doc landed in Firestore with the right shape.
 *   4. Tests idempotency: posts the same order again, confirms no duplicate.
 *   5. Cleans up — revokes the test key, deletes the test shipment.
 *
 * Usage:
 *   node scripts/test-merchant-webhook.mjs                     # uses most-recent user
 *   node scripts/test-merchant-webhook.mjs <email-or-uid>      # uses specific user
 *
 * Reads FIREBASE_SERVICE_ACCOUNT_KEY from .env.local. Assumes dev server
 * is running at http://localhost:3000.
 */
import { config } from 'dotenv';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { initializeApp, cert, getApps } from 'firebase-admin/app';
import { getAuth } from 'firebase-admin/auth';
import { getFirestore, Timestamp, FieldValue } from 'firebase-admin/firestore';
import crypto from 'crypto';

const __dirname = dirname(fileURLToPath(import.meta.url));
config({ path: resolve(__dirname, '..', '.env.local') });

const raw = process.env.FIREBASE_SERVICE_ACCOUNT_KEY || '';
if (!raw) {
    console.error('FIREBASE_SERVICE_ACCOUNT_KEY not set in .env.local');
    process.exit(1);
}
const serviceAccount = JSON.parse(raw.replace(/\n/g, '\\n'));
if (!getApps().length) initializeApp({ credential: cert(serviceAccount) });

const auth = getAuth();
const db = getFirestore();
const WEBHOOK_URL = 'http://localhost:3000/api/integrations/orders/webhook';

// ---------- Helpers ----------
function sha256(s) {
    return crypto.createHash('sha256').update(s).digest('hex');
}

function generateRawKey() {
    return 'bj_' + crypto.randomBytes(16).toString('hex');
}

async function findUser(arg) {
    if (arg) {
        if (arg.includes('@')) return auth.getUserByEmail(arg);
        return auth.getUser(arg);
    }
    // No arg → pick the most-recently-signed-in user.
    const list = await auth.listUsers(50);
    const sorted = list.users
        .filter((u) => u.metadata.lastSignInTime)
        .sort(
            (a, b) =>
                new Date(b.metadata.lastSignInTime).getTime() -
                new Date(a.metadata.lastSignInTime).getTime()
        );
    if (sorted.length === 0) throw new Error('No users with sign-in history found');
    return sorted[0];
}

// ---------- Main ----------
const arg = process.argv[2];
const user = await findUser(arg);
console.log(`\n🧪 Test user: ${user.uid} (${user.email || '(no email)'})\n`);

// Step 1 — mint API key directly via Firestore (matches apiKeyService.mintApiKey)
const rawKey = generateRawKey();
const keyHash = sha256(rawKey);
const keyPrefix = rawKey.slice(0, 11);
const keyDocRef = await db.collection('clientApiKeys').add({
    clientId: user.uid,
    hash: keyHash,
    keyPrefix,
    label: 'E2E smoke test',
    createdAt: Timestamp.now(),
});
console.log(`✅ Step 1 — API key minted`);
console.log(`   keyId:   ${keyDocRef.id}`);
console.log(`   key:     ${rawKey}`);
console.log(`   prefix:  ${keyPrefix}`);

// Step 2 — POST a sample order to the webhook
const externalOrderId = `E2E-${Date.now()}`;
const payload = {
    external_order_id: externalOrderId,
    customer: {
        name: 'E2E Test Customer',
        phone: '9000000000',
        email: 'e2e@example.com',
    },
    shipping_address: {
        name: 'E2E Test Customer',
        phone: '9000000000',
        line1: '42 Webhook Lane',
        line2: 'Apt 1',
        city: 'Bangalore',
        state: 'Karnataka',
        pincode: '560001',
        country: 'India',
    },
    items: [
        {
            name: 'Smoke Test Mug',
            sku: 'MUG-E2E-001',
            quantity: 2,
            unit_price: 24950, // ₹249.50 each → ₹499 total
            weight_g: 350,
        },
    ],
    amounts: {
        subtotal: 49900,
        shipping: 0,
        tax: 0,
        discount: 0,
        total: 49900,
    },
    payment_method: 'prepaid',
    notes: 'Generated by test-merchant-webhook.mjs',
};

const headers = {
    'Content-Type': 'application/json',
    'X-Blujay-Api-Key': rawKey,
};
const response1 = await fetch(WEBHOOK_URL, {
    method: 'POST',
    headers,
    body: JSON.stringify(payload),
});
const body1 = await response1.json();

if (response1.status !== 200 || !body1.ok) {
    console.error(`\n❌ Step 2 — webhook POST failed (HTTP ${response1.status})`);
    console.error(body1);
    process.exit(1);
}
const shipmentId = body1.shipmentId;
console.log(`\n✅ Step 2 — webhook POST succeeded`);
console.log(`   external_order_id: ${externalOrderId}`);
console.log(`   shipmentId:        ${shipmentId}`);
console.log(`   referenceNo:       ${body1.referenceNo}`);

// Step 3 — verify the shipment doc in Firestore matches what we sent
const shipDoc = await db.collection('shipments').doc(shipmentId).get();
if (!shipDoc.exists) {
    console.error(`\n❌ Step 3 — shipment doc not found at ${shipmentId}`);
    process.exit(1);
}
const ship = shipDoc.data();
const expectedChecks = [
    ['clientId', ship.clientId === user.uid],
    ['status', ship.status === 'webhook_pending'],
    ['webhookSource', ship.webhookSource === 'merchant_api'],
    ['webhookExternalOrderId', ship.webhookExternalOrderId === externalOrderId],
    ['webhookApiKeyId', ship.webhookApiKeyId === keyDocRef.id],
    ['destination.pincode', ship.destination?.pincode === '560001'],
    ['products[0].name', ship.products?.[0]?.name === 'Smoke Test Mug'],
    ['products[0].quantity', ship.products?.[0]?.quantity === 2],
    ['referenceNo', ship.referenceNo === `ORD-${externalOrderId}`],
    ['weight (≥0.7kg)', (ship.weight || 0) >= 0.7], // 2 × 350g = 700g
];

console.log(`\n✅ Step 3 — shipment doc validation:`);
let allPass = true;
for (const [label, ok] of expectedChecks) {
    console.log(`   ${ok ? '✓' : '✗'} ${label}`);
    if (!ok) allPass = false;
}
if (!allPass) {
    console.error('\n❌ One or more checks failed — bailing before cleanup.');
    process.exit(1);
}

// Step 4 — idempotency: post the SAME order again, expect no new shipment
const response2 = await fetch(WEBHOOK_URL, {
    method: 'POST',
    headers,
    body: JSON.stringify(payload),
});
const body2 = await response2.json();
if (body2.shipmentId !== shipmentId || !body2.idempotent) {
    console.error(`\n❌ Step 4 — idempotency check failed`);
    console.error('   expected idempotent=true and same shipmentId');
    console.error(body2);
    process.exit(1);
}
console.log(`\n✅ Step 4 — idempotency confirmed (re-post returned same shipmentId, flagged idempotent)`);

// Step 5 — auth gate: a bogus key must be rejected
const responseBad = await fetch(WEBHOOK_URL, {
    method: 'POST',
    headers: {
        'Content-Type': 'application/json',
        'X-Blujay-Api-Key': 'bj_bogus00000000000000000000000000',
    },
    body: JSON.stringify(payload),
});
if (responseBad.status !== 401) {
    console.error(
        `\n❌ Step 5 — auth gate broken (bogus key got HTTP ${responseBad.status} instead of 401)`
    );
    process.exit(1);
}
console.log(`\n✅ Step 5 — auth gate working (bogus key rejected with 401)`);

// Step 6 — revoke the key, then confirm it stops working
await keyDocRef.update({ revokedAt: Timestamp.now() });
const responseRevoked = await fetch(WEBHOOK_URL, {
    method: 'POST',
    headers,
    body: JSON.stringify({ ...payload, external_order_id: `E2E-revoke-${Date.now()}` }),
});
if (responseRevoked.status !== 401) {
    console.error(
        `\n❌ Step 6 — revoked key still accepted (HTTP ${responseRevoked.status})`
    );
    process.exit(1);
}
console.log(`\n✅ Step 6 — revoked key rejected with 401`);

// Step 7 — clean up the test shipment so it doesn't pollute the UI
await db.collection('shipments').doc(shipmentId).delete();
await keyDocRef.delete();
console.log(`\n✅ Step 7 — cleanup done (test shipment + test key deleted)\n`);

console.log('━'.repeat(60));
console.log('🎯  ALL CHECKS PASSED — Phase 10 + 11 merchant webhook is working');
console.log('━'.repeat(60));
console.log();
process.exit(0);
